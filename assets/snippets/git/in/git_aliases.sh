```bash
git config --global alias.br branch                                       # शॉर्टकट: git branch
git config --global alias.ci commit                                       # शॉर्टकट: git commit
git config --global alias.co checkout                                     # शॉर्टकट: git checkout
git config --global alias.graph "log --oneline --graph --all --decorate"  # सुंदर इतिहास ग्राफ
git config --global alias.last "log -1 HEAD"                              # अंतिम कमिट दिखाएँ
git config --global alias.st status                                       # शॉर्टकट: git status
```