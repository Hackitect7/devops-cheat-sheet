```bash
git config --global alias.br branch                                       # сокращение для: git branch
git config --global alias.ci commit                                       # сокращение для: git commit
git config --global alias.co checkout                                     # сокращение для: git checkout
git config --global alias.graph "log --oneline --graph --all --decorate"  # наглядный граф истории
git config --global alias.last "log -1 HEAD"                              # показать последний коммит
git config --global alias.st status                                       # сокращение для: git status
```