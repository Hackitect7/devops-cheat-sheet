```bash
git config --global alias.br branch                                       # শর্টকাট: git branch
git config --global alias.ci commit                                       # শর্টকাট: git commit
git config --global alias.co checkout                                     # শর্টকাট: git checkout
git config --global alias.graph "log --oneline --graph --all --decorate"  # সুন্দর ইতিহাস গ্রাফ
git config --global alias.last "log -1 HEAD"                              # সর্বশেষ কমিট দেখাও
git config --global alias.st status                                       # শর্টকাট: git status
```