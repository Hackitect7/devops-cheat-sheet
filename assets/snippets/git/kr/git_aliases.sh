```bash
git config --global alias.br branch                                       # 단축키: git branch
git config --global alias.ci commit                                       # 단축키: git commit
git config --global alias.co checkout                                     # 단축키: git checkout
git config --global alias.graph "log --oneline --graph --all --decorate"  # 보기 좋은 히스토리 그래프
git config --global alias.last "log -1 HEAD"                              # 마지막 커밋 표시
git config --global alias.st status                                       # 단축키: git status
```